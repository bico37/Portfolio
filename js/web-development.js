// Funktion zur Umwandlung des Datums in ein standardisiertes Format (yyyy-mm)
function parseDate(dateStr) {
  const dateRange = dateStr.split(" - "); // Teilt bei ZeitrÃ¤umen in Start und Ende
  const dateParts = (dateRange[1] || dateRange[0]).split(" "); // Nimmt das Ende, falls vorhanden
  const months = {
    January: "01",
    February: "02",
    March: "03",
    April: "04",
    May: "05",
    June: "06",
    July: "07",
    August: "08",
    September: "09",
    October: "10",
    November: "11",
    December: "12",
  };

  return `${dateParts[1]}-${months[dateParts[0]]}`; // yyyy-mm
}

// Projekte nach Datum sortieren (neueste zuerst)
projects.sort((a, b) => {
  const dateA = parseDate(a.date);
  const dateB = parseDate(b.date);

  return dateB.localeCompare(dateA); // Neueste zuerst
});

